    };
  },
  computed: {
    user() {
      return this.$store.state.user;
    },
    status() {
      return this.statuses[this.$store.state.user.applicationStatus];
    }
  },
  mounted() {
    let now = new Date().getTime();
    let countDownDate = new Date("Nov 14, 2021, 14:30:00").getTime();
    let eventTime = new Date("Nov 13, 2021, 9:00:00").getTime();
    if (eventTime - now > 0) {
      this.title = "UNTIL BOSTONHACKS";
      countDownDate = eventTime;
    } else {
      this.title = "UNTIL SUBMISSIONS DUE";
    }
    this.timerCount(countDownDate);
    this.interval = setInterval(() => {
      this.timerCount(countDownDate);
    }, 1000);
  },
  methods: {
    prefixZero(num) {
      return String(num).padStart(2, "0");
    },
    timerCount(countDownDate) {
      let now = new Date().getTime();
      let distance = countDownDate - now;

      let days = Math.floor(distance / (1000 * 60 * 60 * 24));
      let hours = Math.floor((distance / (1000 * 60 * 60)) % 24);
      let minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
      let seconds = Math.floor((distance % (1000 * 60)) / 1000);

      if (parseInt(days) * 24 + parseInt(hours) > 99) {
        this.time_1 = this.prefixZero(days);
        this.time_2 = this.prefixZero(hours);
        this.time_3 = this.prefixZero(minutes);
      } else {
        this.time_1 = this.prefixZero(days * 24 + hours);
        this.time_2 = this.prefixZero(minutes);
        this.time_3 = this.prefixZero(seconds);
      }
    }
  }
};
</script>
